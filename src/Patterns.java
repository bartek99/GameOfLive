/**The interface has declared methods of patterns
 * @version 1.0
 * @author Bartosz Liszka
 */
public interface Patterns {
    /**
     * {@inheritDoc}
     * Pattern which has 50% alive cells and 50% dead cells
     */
    public void randomPattern();

    /**
     * {@inheritDoc}
     * Pattern of queen Bee Schuttle
     */
    public void queenBeeShuttlePattern();

    /**
     * {@inheritDoc}
     * Pattern of tumbler
     */
    public void tumblerPattern();
}